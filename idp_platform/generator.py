def generate_service(
    service_name: str,
    team: str,
    language: str,
    namespace: str,
    repo_url: str = "https://github.com/chku3401/idp-platform-demo",
) -> dict:
    """Generate a full service scaffold. Returns a summary dict.

    Raises ServiceGenerationError for invalid input, ServiceExistsError if the
    target directory already exists.
    """
    validate_request(service_name, team, language, namespace)

    base = GENERATED_DIR / service_name
    if base.exists():
        raise ServiceExistsError(f"Service '{service_name}' already exists at {base}")

    fmt = dict(service_name=service_name, team=team, language=language, namespace=namespace)

    _copy_source_template(service_name, team, language, base)

    dockerfile = _DOCKERFILE_JAVA if language == "java" else _DOCKERFILE_NODE
    (base / "Dockerfile").write_text(dockerfile)
    (base / "catalog-info.yaml").write_text(_CATALOG_INFO.format(**fmt))
    (base / "README.md").write_text(_SERVICE_README.format(**fmt))

    helm_dir = base / "helm"
    helm_dir.mkdir(parents=True, exist_ok=True)
    (helm_dir / "values.yaml").write_text(_HELM_VALUES.format(**fmt))

    gitops_dir = base / "gitops"
    gitops_dir.mkdir(parents=True, exist_ok=True)
    (gitops_dir / "application.yaml").write_text(_ARGO_APP.format(repo_url=repo_url, **fmt))

    WORKFLOWS_DIR.mkdir(parents=True, exist_ok=True)
    ci_template = _CI_JAVA if language == "java" else _CI_NODE
    (WORKFLOWS_DIR / f"{service_name}.yaml").write_text(ci_template.format(**fmt))

    return {
        "service": service_name,
        "team": team,
        "namespace": namespace,
        "language": language,
        "path": str(base.relative_to(REPO_ROOT)),
        "generated_items": [
            "source template",
            "Dockerfile",
            "catalog-info.yaml",
            "helm/values.yaml",
            "gitops/application.yaml",
            f".github/workflows/{service_name}.yaml",
        ],
    }
