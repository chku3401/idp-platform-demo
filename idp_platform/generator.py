import re
import shutil
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parent.parent
TEMPLATES_DIR = REPO_ROOT / "templates"
GENERATED_DIR = REPO_ROOT / "generated"
WORKFLOWS_DIR = REPO_ROOT / ".github" / "workflows"

VALID_LANGUAGES = {"java", "node"}
NAME_PATTERN = re.compile(r"^[a-z][a-z0-9-]{1,38}[a-z0-9]$")

_LANGUAGE_DIRS = {
    "java": "java-springboot",
    "node": "node-service",
}

_PLACEHOLDER_FILE_SUFFIXES = {
    ".json", ".js", ".xml", ".java", ".properties", ".md",
}


class ServiceGenerationError(ValueError):
    """Raised for invalid input (bad name/language/team/namespace)."""


class ServiceExistsError(FileExistsError):
    """Raised when the target service directory already exists."""


def validate_request(service_name: str, team: str, language: str, namespace: str) -> None:
    if language not in VALID_LANGUAGES:
        raise ServiceGenerationError(
            f"Unsupported language '{language}'. Must be one of: {sorted(VALID_LANGUAGES)}"
        )
    if not NAME_PATTERN.match(service_name):
        raise ServiceGenerationError(
            "service_name must be lowercase alphanumeric with hyphens, "
            "3-40 chars, and cannot start/end with a hyphen (e.g. 'payment-api')"
        )
    if not team or not re.match(r"^[a-z][a-z0-9-]*$", team):
        raise ServiceGenerationError("team must be a lowercase slug (e.g. 'payments')")
    if not namespace or not re.match(r"^[a-z][a-z0-9-]*$", namespace):
        raise ServiceGenerationError("namespace must be a lowercase slug (e.g. 'payments-dev')")


def _substitute(text: str, service_name: str, team: str) -> str:
    return text.replace("__SERVICE_NAME__", service_name).replace("__TEAM__", team)


def _copy_source_template(service_name: str, team: str, language: str, dest: Path) -> None:
    src = TEMPLATES_DIR / _LANGUAGE_DIRS[language]
    for path in src.rglob("*"):
        rel = path.relative_to(src)
        target = dest / rel
        if path.is_dir():
            target.mkdir(parents=True, exist_ok=True)
            continue
        target.parent.mkdir(parents=True, exist_ok=True)
        if path.suffix in _PLACEHOLDER_FILE_SUFFIXES:
            content = path.read_text()
            target.write_text(_substitute(content, service_name, team))
        else:
            shutil.copyfile(path, target)


_DOCKERFILE_JAVA = """FROM maven:3.9-eclipse-temurin-17 AS build
WORKDIR /app
COPY pom.xml .
COPY src ./src
RUN mvn -q -DskipTests package

FROM eclipse-temurin:17-jre
WORKDIR /app
COPY --from=build /app/target/*.jar app.jar
EXPOSE 8080
ENTRYPOINT ["java", "-jar", "app.jar"]
"""

_DOCKERFILE_NODE = """FROM node:20-alpine
WORKDIR /app
COPY package*.json ./
RUN npm install --omit=dev
COPY . .
EXPOSE 8080
CMD ["npm", "start"]
"""

